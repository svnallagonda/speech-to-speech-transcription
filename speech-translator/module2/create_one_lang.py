def create_lang_audio(lang_code, num_samples=20, output_dir="data"):
    """Create audio files for ONE language."""
    if lang_code not in TEST_PHRASES:
        print(f"Error: Language code '{lang_code}' not found.")
        print(f"Available codes: {', '.join(TEST_PHRASES.keys())}")
        return 0
    
    lang_info = TEST_PHRASES[lang_code]
    lang_name = lang_info["name"]
    phrases = lang_info["phrases"]
    
    os.makedirs(output_dir, exist_ok=True)
    
    print(f"Creating {num_samples} audio files for {lang_name} ({lang_code})...\n")
    
    total = 0
    for i in range(num_samples):
        phrase = phrases[i % len(phrases)]
        
        try:
            # Try the language code first
            try:
                tts = gTTS(text=phrase, lang=lang_code, slow=False)
            except:
                # For unsupported codes, try fallback or auto-detect
                if lang_code == "pa":
                    # Punjabi - try using 'hi' as fallback (closest match)
                    try:
                        tts = gTTS(text=phrase, lang='hi', slow=False)
                    except:
                        # If that fails, try auto-detect
                        tts = gTTS(text=phrase, lang=None, slow=False)
                elif lang_code == "or":
                    # Odia - try using 'hi' as fallback
                    try:
                        tts = gTTS(text=phrase, lang='hi', slow=False)
                    except:
                        tts = gTTS(text=phrase, lang=None, slow=False)
                else:
                    # For other languages, try auto-detect
                    tts = gTTS(text=phrase, lang=None, slow=False)
            
            mp3_path = os.path.join(output_dir, f"sample_{lang_code}_{i:03d}.mp3")
            tts.save(mp3_path)
            total += 1
            
            if (i + 1) % 5 == 0:
                print(f"  Created {i + 1}/{num_samples} files...")
            
            time.sleep(0.5)
            
        except Exception as e:
            print(f"  Warning: Could not create file {i}: {e}")
            continue
    
    print(f"\nComplete! {total} files created for {lang_name}")
    print(f"Files saved in: {os.path.abspath(output_dir)}")
    return total

if __name__ == "__main__":
    if len(sys.argv) < 2:
        print("Usage: python create_one_lang.py <lang_code>")
        print(f"\nAvailable language codes:")
        for code, info in TEST_PHRASES.items():
            print(f"  {code} - {info['name']}")
        print("\nExample: python create_one_lang.py hi")
    else:
        lang_code = sys.argv[1].lower()
        create_lang_audio(lang_code, num_samples=20)

